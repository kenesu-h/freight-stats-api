CREATE TABLE shipment (
  shipment_id INT PRIMARY KEY,
  trade_type TINYINT NOT NULL,
  commodity_type SMALLINT NOT NULL,
  transport_method SMALLINT NOT NULL,
  source_state SMALLINT NOT NULL,
  destination_state SMALLINT NOT NULL,
  value BIGINT NOT NULL,
  weight BIGINT NOT NULL,
  freight_charges int NOT NULL,
  df TINYINT,
  containerized TINYINT NOT NULL,
  ship_date DATE NOT NULL,
  CONSTRAINT fk_commodity FOREIGN KEY (commodity_type) REFERENCES commodity (commodity_id),
  CONSTRAINT fk_transport_method FOREIGN KEY (transport_method) REFERENCES transport_method (transport_method_id),
  CONSTRAINT fk_source_state FOREIGN KEY (source_state) REFERENCES state (state_id),
  CONSTRAINT fk_dest_state FOREIGN KEY (destination_state) REFERENCES state (state_id)
) ;

